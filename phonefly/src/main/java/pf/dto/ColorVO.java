package pf.dto;

public class ColorVO {
	
	private Integer ceseq;
	private String name;
	private String ccode;
	
	public Integer getCeseq() {
		return ceseq;
	}
	public void setCeseq(Integer ceseq) {
		this.ceseq = ceseq;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCcode() {
		return ccode;
	}
	public void setCcode(String ccode) {
		this.ccode = ccode;
	}
	
}
